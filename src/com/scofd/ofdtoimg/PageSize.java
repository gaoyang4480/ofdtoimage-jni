package com.scofd.ofdtoimg;

public class PageSize {
    /**
     * 宽度
     */
    private float width;
    /**
     * 高度
     */
    private float height;

    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }
}
